
def fn1(x: int) -> int:
    """
    function to square integer
    :param x: integer
    :return: x squared
    """
    return x * x


def fn2(x: int) -> int:
    """
    function to double integer
    :param x: integer
    :return: 2x
    """
    return 2 * x


def fn3(x: int) -> float:
    """
    function to halve integer
    :param x:
    :return: x/2
    """
    return x / 2
